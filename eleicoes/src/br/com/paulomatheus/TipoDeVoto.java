package br.com.paulomatheus;

public class TipoDeVoto {

	private int quantidadeDeTotal = 1000;
	private int quantidadeDeValidos = 800;
	private int quantidadeDeBrancos = 150;
	private int quantidadeDeNulos = 50;
	
	public int getQuantidadeDeTotal() {
		return quantidadeDeTotal;
	}
	public void setQuantidadeDeTotal(int quantidadeDeTotal) {
		this.quantidadeDeTotal = quantidadeDeTotal;
	}
	public int getQuantidadeDeValidos() {
		return quantidadeDeValidos;
	}
	public void setQuantidadeDeValidos(int quantidadeDeValidos) {
		this.quantidadeDeValidos = quantidadeDeValidos;
	}
	public int getQuantidadeDeBrancos() {
		return quantidadeDeBrancos;
	}
	public void setQuantidadeDeBrancos(int quantidadeDeBrancos) {
		this.quantidadeDeBrancos = quantidadeDeBrancos;
	}
	public int getQuantidadeDeNulos() {
		return quantidadeDeNulos;
	}
	public void setQuantidadeDeNulos(int quantidadeDeNulos) {
		this.quantidadeDeNulos = quantidadeDeNulos;
	}
	

	
	
	
	
	
}
